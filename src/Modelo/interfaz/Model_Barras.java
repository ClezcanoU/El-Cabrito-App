
package Modelo.interfaz;

public class Model_Barras {
    
    private int totalMesasLibres;
    private int totalMesasOcupadas;
    private int totalMesasMantenimiento;
    private int totalmesas;

    public int getTotalMesasLibres() {
        return totalMesasLibres;
    }

    public void setTotalMesasLibres(int totalMesasLibres) {
        this.totalMesasLibres = totalMesasLibres;
    }

    public int getTotalMesasOcupadas() {
        return totalMesasOcupadas;
    }

    public void setTotalMesasOcupadas(int totalMesasOcupadas) {
        this.totalMesasOcupadas = totalMesasOcupadas;
    }

    public int getTotalMesasMantenimiento() {
        return totalMesasMantenimiento;
    }

    public void setTotalMesasMantenimiento(int totalMesasMantenimiento) {
        this.totalMesasMantenimiento = totalMesasMantenimiento;
    }

    public int getTotalmesas() {
        return totalmesas;
    }

    public void setTotalmesas(int totalmesas) {
        this.totalmesas = totalmesas;
    }
    
}
